def DFS(v):
    stack = [v]  # λμ•„κ° κ³³μ„ κΈ°λ΅ν•λ” λ¦¬μ¤νΈ
    visited[v] = 1  # λ°©λ¬Έ μ²λ¦¬

    while stack:  # μ¤νƒμ΄ λΉ„μ–΄μμ„ λ•κΉμ§€ λ°λ³µ
        cur = stack.pop()  # μ •μ μ„ λΉΌλ©΄μ„ λ³€μμ— μ €μ¥

        for d in JOIN[cur]:  # λΊ€ μ •μ κ³Ό μΈμ ‘ν• λ¨λ“  μ •μ  ν™•μΈ
            if not visited[d]:  # λ°©λ¬Έμ„ ν•μ§€ μ•μ€ μ •μ μ΄λΌλ©΄
                visited[d] = 1  # λ°©λ¬Έ μ²λ¦¬
                stack.append(d)  # μ¤νƒμ— μ¶”κ°€


